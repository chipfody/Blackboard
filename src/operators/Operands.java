package operators;

public class Operands {

    public static void main(String[] args) {
        char char1 = 'a';
        System.out.println(char1);
        System.out.println(char1 + char1);
        System.out.println(char1 - char1);

        final byte age1 = 10;
        final byte age2 = 5;
        short sum = age1 + age2;
        System.out.println("\n" + sum);
    }
}
